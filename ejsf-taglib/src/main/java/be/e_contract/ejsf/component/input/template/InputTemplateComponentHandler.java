package be.e_contract.ejsf.component.input.template;

import javax.faces.view.facelets.ComponentConfig;
import javax.faces.view.facelets.ComponentHandler;
import javax.faces.view.facelets.MetaRule;
import javax.faces.view.facelets.MetaRuleset;

public class InputTemplateComponentHandler extends ComponentHandler {

    private static final MetaRule RESULT_META_RULE = new ResultMetaRule();

    public InputTemplateComponentHandler(ComponentConfig config) {
        super(config);
    }

    @Override
    protected MetaRuleset createMetaRuleset(Class type) {
        MetaRuleset metaRuleset = super.createMetaRuleset(type);
        metaRuleset.addRule(RESULT_META_RULE);
        return metaRuleset;
    }
}
